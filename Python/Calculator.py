import tkinter as tk

#/////////////////////////////////////////////////////////////////////////////////////////////////////////

calculation = ""

#/////////////////////////////////////////////////////////////////////////////////////////////////////////

def addToCalculation(symbol):

    global calculation
    calculation += str(symbol)
    textResults.delete(1.0, "end") 
    textResults.insert(1.0, calculation)

#/////////////////////////////////////////////////////////////////////////////////////////////////////////

def evaluateCalculation():

    global calculation

    try: 

        calculation = str(eval(calculation))
        textResults.delete(1.0, "end")
        textResults.insert(1.0, calculation)

    except:

        clearField()
        textResults.insert(1.0, "Error")

#/////////////////////////////////////////////////////////////////////////////////////////////////////////

def clearField():

    global calculation
    calculation = ""
    textResults.delete(1.0, "end")

#/////////////////////////////////////////////////////////////////////////////////////////////////////////

root = tk.Tk()
root.title("Calculator") #v1.0
root.geometry("351x290")
root.resizable(False, False)  # Prevent resizing
textResults = tk.Text(root, height=2, width=16, font=("ariel", 24))
textResults.grid(columnspan=9)

